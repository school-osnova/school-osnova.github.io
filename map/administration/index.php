							<h4><strong>
								Любімова Алла Володимирівна, за фахом вчитель початкових класів, працює директором ЗНВК «ОСНОВА» (з 2015 року - ЗЗОШ «ОСНОВА») з 2009 року.

             					Вектор управлінських дій директора ЗЗОШ "ОСНОВА" Любімової А.В. спрямований на систему загальної середньої освіти, її адаптацію до соціально-демографічної, економічної ситуації.

             					На  виконання Постанови Кабінету Міністрів України від 16.11.2000 року № 1717 “Про перехід загальноосвітніх навчальних закладів на новий зміст, структуру і 12-річний термін навчання”, Концепції профільного навчання та з метою формування системи профільних знань не з одного профілю, а з декількох, що сприяє розвитку загальнокультурного рівня випускника, його компетентності та конкурентоспроможності 100 % учнів старшої школи охоплено профільним навчанням. Для учнів 8-9 класів забезпечується допрофільна підготовка.

              					В чотирьох класах створено умови для поглибленого вивчення іноземних мов (англійської, німецької, французької), що сприяє формуванню стійкого інтересу до предмету, розвитку відповідних компетентностей.

                				Діяльність загальноосвітнього закладу середньої освіти організовується на засадах демократизму, гуманізму, на поєднанні державного управління та громадського самоврядування, впровадження колегіального стилю управління.

           						ЗЗОШ "ОСНОВА"  є учасником науково -педагогічного експерименту Міністерства освіти і науки України та пілотного проєкту Благодійного Фонду Порошенка "Розвиток інклюзивного освітнього середовища в Запорізькій області", Всеукраїнського експерименту "Е-підручник", Всеукраїнського експерименту " Розроблення і впровадження навчально-методичного забезпечення інтегрованого курсу "Природничі науки" для 10-11 класів закладів загальної середньої освіти". Другий рік поспіль впроваджується Концепція Нової української школи у початкових класах.

                 				У школі функціонує 22 класи, з них 9 інклюзивних класи;  3 класи для дітей із затримкою психічного розвитку. Всього учнів - 508.

                				У школі навчається 42 учні, які мають статус дитини з інвалідністю, тому освітній процес базується на засадах інклюзивної освіти, забезпечується медичний та психолого – педагогічний супровід.
